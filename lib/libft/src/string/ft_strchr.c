#include "libft.h"

int	ft_strchr(const char *s, int c)
{
	if (!s || !c)
		return (0);
	while (*s && *s != (char)c)
	{
		if (*s == (char)c || !c)
			return (1);
		s++;
	}
	
	return (0);
}
