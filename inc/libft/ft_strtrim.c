#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)

{
	char	*str;
	int		j;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	j = ft_strlen(s1);
	while (*s1 && ft_strchr(set, s1[j]))
		j--;
	str = ft_substr(s1, 0, j + 1);
	return (str);
}
